import turtle
james=turtle.Turtle()
def square (color):
    james.color(color)
    james.forward(100)
    james.right(90)
    james.forward(100)
    james.right(90)
    james.forward(100)
    james.right(90)
    james.forward(100)
square("red")
square("orange")
square("yellow")
square("green")
square("blue")
square("indigo")
square("violet")



    
    
